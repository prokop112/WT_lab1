package by.bsuir.classes_and_objects.task9;

public class Ball {
    private int weight;
    private String color;

    public Ball(int weight, String color) {
        this.weight = weight;
        this.color = color;
    }

    public int getWeight() {
        return this.weight;
    }

    public String getColor() {
        return this.color;
    }
}
